package com.siyuan.enjoyreading.entity;

public class SmallCategoryItem {

    private String name;
    private String icon_url;
    private String badge_text;
    private String app_jump;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIcon_url() {
        return icon_url;
    }

    public void setIcon_url(String icon_url) {
        this.icon_url = icon_url;
    }

    public String getBadge_text() {
        return badge_text;
    }

    public void setBadge_text(String badge_text) {
        this.badge_text = badge_text;
    }

    public String getApp_jump() {
        return app_jump;
    }

    public void setApp_jump(String app_jump) {
        this.app_jump = app_jump;
    }
}
